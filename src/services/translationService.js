export const formatMoney = (amount, currencyCode) => {
    try {
        return new Intl.NumberFormat(undefined, {
            style: 'currency',
            currency: currencyCode,
        }).format(amount);
    }
    catch (e) {
        // Fallback if currency code is invalid
        return `${amount.toFixed(2)} ${currencyCode}`;
    }
};
